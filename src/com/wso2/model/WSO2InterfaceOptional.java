package com.wso2.model;

public class WSO2InterfaceOptional {

    private WSO2Interface wso2Interface;
    private String handling;
    private String handlingTime;


    public WSO2Interface getWso2Interface() {
        return wso2Interface;
    }

    public void setWso2Interface(WSO2Interface wso2Interface) {
        this.wso2Interface = wso2Interface;
    }

    public String getHandling() {
        return handling;
    }

    public void setHandling(String handling) {
        this.handling = handling;
    }

    public String getHandlingTime() {
        return handlingTime;
    }

    public void setHandlingTime(String handlingTime) {
        this.handlingTime = handlingTime;
    }
}
